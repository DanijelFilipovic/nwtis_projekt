package org.foi.nwtis.dfilipov.web.data;

public class User
{	
	private int id;
	private String username;

	public User() {}

	public Integer getId()
	{
		return id;
	}

	public void setId(Integer id)
	{
		this.id = id;
	}

	public String getUsername()
	{
		return username;
	}

	public void setUsername(String username)
	{
		this.username = username;
	}
}
